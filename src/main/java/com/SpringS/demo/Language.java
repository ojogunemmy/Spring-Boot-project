package com.SpringS.demo;

public class Language {
	String lang;
	String action;
	
	


	public Language(String lang,String action) {
		this.lang=lang;
		this.action = action;
	}

	public String getLang() {
		return lang;
	}
	public void setLang(String action) {
		this.action = action;
	}


	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

	
	

}
